CREATE TABLE `deceased` (
  `SSN` int NOT NULL,
  `PERSON_ID` int NOT NULL,
  `SERVICE_NUM` int NOT NULL,
  `INFORMANT_ID` int NOT NULL,
  `AGE` int NOT NULL,
  `SEX` char(1) DEFAULT NULL,
  `EYE_COLOR` varchar(45) NOT NULL,
  `RACE` varchar(45) NOT NULL,
  `BIRTHDATE` date NOT NULL,
  `DEATHDATE` date NOT NULL,
  `BIRTH_PLACE` varchar(45) NOT NULL,
  `DEATH_PLACE` varchar(45) DEFAULT NULL,
  `FATHER_NAME` varchar(45) DEFAULT NULL,
  `MOTHER_NAME` varchar(45) DEFAULT NULL,
  `FAMILYHQ` varchar(45) DEFAULT NULL,
  `HQ_PHONENUM` varchar(45) DEFAULT NULL,
  PRIMARY KEY (`SSN`,`PERSON_ID`),
  UNIQUE KEY `SSN_UNIQUE` (`SSN`),
  UNIQUE KEY `PERSON_ID_UNIQUE` (`PERSON_ID`),
  UNIQUE KEY `SERVICE_NUM_UNIQUE` (`SERVICE_NUM`),
  KEY `PERSON_NAME_idx` (`PERSON_ID`),
  KEY `INFORMANT_ID_idx` (`INFORMANT_ID`),
  CONSTRAINT `DECEASED_PERSON_ID` FOREIGN KEY (`PERSON_ID`) REFERENCES `person` (`PERSON_ID`),
  CONSTRAINT `INFORMANT_ID` FOREIGN KEY (`INFORMANT_ID`) REFERENCES `person` (`PERSON_ID`),
  CONSTRAINT `SERVICE_ID` FOREIGN KEY (`SERVICE_NUM`) REFERENCES `service` (`SERVICE_NUM`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci
